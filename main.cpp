#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int num1 = 10, num2 = 5;

    cout << (num1 > num2) << endl;
    cout << (num1 < num2) << endl;
    cout << (num1 >= num2) << endl;
    cout << (num1 <= num2) << endl;
    cout << (num1 == num2) << endl;
    cout << (num1 != num2) << endl;

    return 0;
}